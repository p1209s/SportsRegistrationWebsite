        <h1>Volleyball Rules and Regulations</h1></div>

<ul>
    <h2><li>The Team</li></h2></ul>
<ol>
<li>Six (6) players constitute a team. Each team must have a minimum of two (2)
female players on the court at all times. The same rule applies even if a team
has only five players. Maximum number of players on a team is capped at eight
(8) people.</li>
<li>Each team will have two (2) timeouts of 30 seconds in duration per game. In
an event of an injury, teams will have a 30-second period to decide whether to
take a timeout or to make a legal substitution. If a timeout is taken, that timeout
will be counted toward one of team’s legal timeouts.</li>
<li>A paper/rock/scissors game between the teams or coin toss by a referee will
decide which team will get the choice of service, receive, or court. In the second
game, courts will be switched and the receiving team in the first set will serve
first. If a third game is necessary, the choice of serve, receive, or court will be
decided by another game of paper/rock/scissors or coin toss by a referee and
courts will be changed when a team reaches first 8 points in a tie-breaker match
    of 15 points.</li></ol>

<ul><h2><li>During the Game</li></h2></ul>
    <ul><h4>Rotation</h4><li> Players in a team being awarded for service shall immediately
rotate positions in a clockwise direction when the team wins the “side-out”. At
the time of the serve, players must remain in their positions, but they can switch
positions after the ball is contacted for a serve. Female players must be at
opposite positions on the court at all times if only two female players are used for
    a team.</li><br>
        <h4>Substitution</h4> <li>Teams are permitted to substitute players on any given
position on the court, as long as they are listed on the submitted roster.
However, all substitutions must occur before the start of the referee’s whistle for
a service and must be recognized and waved in by the referee prior to the player
entering the court. In addition, substitutions are permitted for players for a given
position, meaning if Player A substituted for Player B, then Player A can only
substitute for Player B’s position and no other position on the court during that
game. However, teams can freely substitute non-substituted players for Player
A’s position without having to substitute back in the original player, Player B, to
occupy that position.</li><br>
        <h4>Service</h4> <li>Players cannot cross the serving line when serving. If the line is stepped on or crossed over, a “side-out” will be called against that team. Also
jump serves are allowed but players need to jump from behind the serving line. If
a player is a wrong server, “side-out” will be called and a point will be awarded to
the opposing team. A served ball that hits the net but crosses over the net and is
in the bounds of the court is considered still in play. If the ball does not cross
over the net, it is a “side out”, and a point will be awarded to the opposing team.
The contact of ball for a service must be made with one hand or forearm with a
complete release of ball prior to contacting the ball, and the service must be
executed within 8 seconds of the referee’s whistle for a service. A failure to
execute the service properly will result in awarding of a point and the side out to
the opposing team.
</li><br>
        <h4>Playing the Ball</h4><li> A ball may be hit only three times prior to crossing over the
vertical plane of the net. A block is not counted as a hit. A player cannot hit the
ball twice with one attempt to hit the ball except on a team’s first contact of the
ball. The ball must be clearly hit, either by one or both hands (open or closed), or
with any part of the body. It is illegal to “push”, “carry”, or “lift” the ball. Spiking is
legal.
</li><br>
        <h4>Coed Rule</h4><li> The coed volleyball rule is NOT in effect. When a ball is hit more
than once before going over the net, a female does not need to contact the ball.
</li><br>
        <h4>Simultaneous Hit</h4> <li>If the ball is hit at the same time by two players, either
one of the players can hit the ball again. The side that the ball is pushed out to
will be awarded the side-out point.
</li><br>
        <h4>Net Play</h4><li> If a player contacts the TOP of the net between the antennae at
any time other than after the play is over, it is a net fault which results in
awarding a point and the side out to the team with no net fault. It is not a net
fault if the player’s hairs brush onto the net. It is also not a net fault if the player
has touched the net as a result of the opposing team hitting the ball into the net,
causing the net to touch a player on the other side by accident. However, if the
player moved in an attempt to touch the ball below the height of the net that
interfered with the opposing team’s ability to play the ball, it is a net fault and a
“side-out” will be awarded to the opposing team.
</li><br>
<h4>Plane of the Net</h4> <li>There is an imaginary plane extending indefinitely above
the net between antennae.<ul>
<li>
<h4>For Attackers:</h4> It would not be a violation, if the player attacking of the ball
attacks the ball on his or her side of the plane, and then follow through
with hands or arms to penetrate this plane.</li>
<li><h4>For Blockers:</h4> It is a reach over violation for the blockers to reach over to
block the ball on the opponent’s side of this imaginary plane when it is
opposing team’s second contact and one of opposing players is nearby to
make a play on the ball. It is also a reaching over violation if the player
blocks the ball which is located below the net on the opponent team’s
side, and it is opposing team’s second contact. If it is the 3rd contact of the
opposing team, it is legal for the blocker to penetrate the plane of the net
to block the ball. A player may reach under the net to play the ball,
provided that he or she does not interfere with an opponent to legally play
the ball, if the ball has not completely crossed under the net.
</li><br></ul>
        <h4>Center Line</h4> <li> If a player’s ENTIRE hand or foot crosses under the vertical
plane of the net to play a ball or any part of body other than hand or foot, a “sideout” will be called and a point will be awarded to the opposing team.
</li><br>
        <h4>Boundaries</h4><li> If any part of the ball touches a boundary line, the ball is
considered inbounds. A replay will be declared when there is a conflicting call in
which case the referee will be the decision maker. All balls must be returned
above the net and between antennae.
</li><br>
        <h4>Back row spiking</h4><li> Back row spiking is allowed but the player must jump
behind the attack line for a legal back row attack. Players can rotate to any
position on the court during play, but players from the back row will not be able to
jump to block in front of the attack line. A “side-out” will be called if the back row
player blocks the ball. A back row player can attack the ball in front of attack line,
providing that the ball is not completely above the height of the net at the time of
attacking the ball.
</li><br>
        <h4>Ceiling hit rule</h4><li> If the ball hits your side of the ceiling and falls on your side,
the ball is still in play. The play, however, will be considered over if the ball goes
over to the opponent’s side after it has contacted your team’s side of ceiling.</li>
</ul>

