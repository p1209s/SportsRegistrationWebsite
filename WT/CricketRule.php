   <ol>
       <h3><li>Extras:</li></h3>
      <ul>
         <li>Byes and over throws are applicable in this tournament. But LBW is not applicable</li>
         <li>No ball and Wide will be penalized according to tennis ball cricket rules. One run and an extra ball will be awarded for a no ball and a wide ball and No ball will be awarded with a free hit.</li>
         <li>Umpires reserve every right to declare a no ball with or without warning" if any bowler delivers under arm or throw ball.</li>
         <li>If the ball pitches outside the mat it will be called wide with one run and an extra ball.</li>
         <li>If the ball bounces twice before reaching the batsman it will be called a NO ball and every no ball will be awarded with a free hit. Umpires will make the call on this and it will be final decision.</li>
      </ul><br>
       <h3><li>Field restriction:</li></h3>
       <ul>
          <li>After the power play, a minimum of 3 fielders should be inside the fielding circle.</li>
          <li>Only 5 fielders can field on the leg side at any point of time (bowler & keeper are not considered as a fielders in this regard.</li>
       </ul><br>
       <h3><li>Roster & player restrictions:</li></h3>
         <ul>
            <li>Transfer of players between teams is not allowed. If noticed, the team will be disqualified from the tournament without any prior notice.</li>
            <li>A team can choose maximum 15 members squad and a team manager. Any players apart from the squad will not be entertained.</li>
         </ul><br>
       <h3><li>Schedule & Game:</li></h3>
          <ul>
            <li>Teams should report 15 minutes before the start of the play.</li>
            <li>After the toss, the fielding team should set the fielding positions immediately to avoid any delay to begin the match. Batsmen will come to the crease only after the field arrangement.</li>
            <li>Teams are advised to avoid any unnecessary delay before or during the game to maintain our time schedule.</li>
            <li>Organizing committee will reserve full authority to intervene in such case off field and umpires will have full authority to intervene on field.</li>
            <li>Each captain should nominate his playing XI before the toss. No player can be changed after the nomination without the consent of the opposing captain.</li>
            <li>A new (Hard tennis ball) ball will be provided for each innings. In case of loss or damaged ball, umpire will replace the old ball with a new one/replacement ball and dead ball will give in case of out only. (Run out will not consider)</li>
            <li>Umpires decisions will be final throughout the tournament. Any sort of misconducts by any players will result in direct suspension.</li>
            <li>A team must be ready to play two matches in a day, if required.</li>
          </ul><br>
       <h3><li>Substitution:</li></h3>
           <ul>
             <li>Substitute runners are not allowed, unless a batsman gets injured in the field during a particular match and the role of a substitute will be fielding only.</li>
             <li>No batsman can get retired in the middle of his innings. If the batsman is injured he will be considered as out with one dot ball.</li>
           </ul><br>
       <h3><li>Super Over:</li></h3>
            <ul>
              <li>In case of a draw in knock1out stage of a league, ‘Super Over’ will come into play. Each team will have to bowl one over called ‘Super Over’. Only 3 batsmen will be allowed to bat the entire Super Over and 2 batsmen have to be given out to end the one1over innings. In simple words, the match will be of one over per innings and only 3 batsmen batting.</li>
              <li>In case, scores are tied at the end of the Super Overs. The team that lost fewer wickets will be adjudged the winner. In case the scores and wickets lost are the same, then each team will enter the “Death Over" where each team will bowl an over each at a set of stumps (without the batsman) and whoever hits the stumps more times will win the game. In case, both teams are equal at the end of six balls, they will carry on until one team misses the stumps, and the team hitting the stumps will be the winner. Every bowler will bowl only ball in Death Over.</li>
            </ul><br>
       <h3><li>Scoring:</li></h3>
            <ul>
               <li>All teams check if your one member should stand near the scorer. </li>
            </ul><br>
       <h3><li>Umpiring</li></h3>
             <ul>
               <li>Umpires have to note down the time for each innings and make sure it gets completed as specified time period.</li>
               <li>If a game is stopped in between due to any reason (rain,player injury etc.) the game will be rescheduled and resume from where it was stopped. </li>
            </ul><br>
       <h3><li>Over & Bowling limit</li></h3>
             <ul>
               <li>8 Overs per inning and In Final 10 Overs per inning.</li>
               <li>Minimum 4 bowlers need to bowl & In Final Minimum 5 bowlers need to bowl. </li>
               <li>2 Overs maximum a bowler can bowl & In Final two bowler can bowl maximun 3 overs.</li>
             </ul><br>   
       <h3><li>Power Play</li></h3>
             <ul>
               <li> A maximum of 2 fielders can be outside the 30 yard circle and power play should be 1st Two overs of the innings.</li>
             </ul>
